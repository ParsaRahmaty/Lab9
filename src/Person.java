public class Person {
    private String name;
    private String lastName;
    private int yearEntered;
    private int id;

    public String getName() {
        return name;
    }

    public String getLastName() {
        return lastName;
    }

    public int getYearEntered() {
        return yearEntered;
    }

    public int getId() {
        return id;
    }
}
